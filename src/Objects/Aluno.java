package Objects;

public class Aluno {
    public int id;
    public String nome;
    public String data_nasc;
    public int matricula;

    public Aluno(int id, String nome, String data_nasc, int matricula) {
        this.id = id;
        this.nome = nome;
        this.data_nasc = data_nasc;
        this.matricula = matricula;
    }
}
