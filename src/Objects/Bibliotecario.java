package Objects;

public class Bibliotecario {
    public String nome_bibliotecario;
    public int id;
    public String data_nascimento;

    public Bibliotecario(String nome_bibliotecario, int id_bibliotecario, String data_nascimento) {
        this.nome_bibliotecario = nome_bibliotecario;
        this.id = id_bibliotecario;
        this.data_nascimento = data_nascimento;
    }
}
